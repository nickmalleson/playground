def normalise_profile(profile: dict[str, Any], display_name: str) -> dict[str, Any]:
    """Coerce a (possibly Claude-generated) profile into a valid shape.

    Ensures the required keys exist, genres have keys/labels, and every seed
    book has a unique id and a genre that matches one of the declared genres.
    """
    favs = [str(x) for x in profile.get("original_favourites", []) if str(x).strip()]
    genres = []
    seen_keys: set[str] = set()
    for g in profile.get("genres", []):
        key = re.sub(r"[^a-z0-9]+", "-", str(g.get("key", "")).lower()).strip("-")
        if not key or key in seen_keys:
            continue
        seen_keys.add(key)
        genres.append({
            "key": key,
            "label": str(g.get("label", key)).strip() or key,
            "sub": str(g.get("sub", "")).strip(),
        })

    valid_genre_keys = {g["key"] for g in genres}
    fallback_genre = genres[0]["key"] if genres else "general"

    seed_books = []
    taken_ids: set[str] = set()
    for b in profile.get("seed_books", []):
        title = str(b.get("title", "")).strip()
        if not title:
            continue
        genre = str(b.get("genre", "")).strip()
        if genre not in valid_genre_keys:
            genre = fallback_genre
        base = re.sub(r"[^a-z0-9]+", "-", (b.get("id") or title).lower()).strip("-") or "book"
        bid = base
        n = 2
        while bid in taken_ids:
            bid = f"{base}-{n}"
            n += 1
        taken_ids.add(bid)
        seed_books.append({
            "id": bid,
            "title": title,
            "author": str(b.get("author", "Unknown")).strip() or "Unknown",
            "genre": genre,
            "why": str(b.get("why", "")).strip(),
        })

    return {
        "version": 1,
        "display_name": display_name,
        "original_favourites": favs,
        "genres": genres,
        "seed_books": seed_books,
    }


def save_profile(user: str, profile: dict[str, Any]) -> None:
    user_dir(user).mkdir(parents=True, exist_ok=True)
    path = profile_path(user)
    tmp = path.with_suffix(".tmp")
    with tmp.open("w", encoding="utf-8") as f:
        json.dump(profile, f, indent=2, ensure_ascii=False)
    tmp.replace(path)
    log.debug(
        f"saved profile for {user!r}: {len(profile.get('original_favourites', []))} favourites, "
        f"{len(profile.get('genres', []))} genres, {len(profile.get('seed_books', []))} seed books"
    )


def load_profile(user: str) -> dict[str, Any]:
    path = profile_path(user)
    with path.open("r", encoding="utf-8") as f:
        profile = json.load(f)
    for key in ("original_favourites", "genres", "seed_books"):
        if key not in profile:
            raise ValueError(f"profile for {user!r} is missing key: {key}")
    profile.setdefault("display_name", user)
    return profile


# ── State ──

def default_state(profile: dict[str, Any]) -> dict[str, Any]:
    return {
        "version": 1,
        "current_books": [dict(b) for b in profile.get("seed_books", [])],
        "marks": {},
        "history": [],
    }


def load_state(user: str, profile: dict[str, Any]) -> dict[str, Any]:
    path = state_path(user)
    if not path.exists():
        log.info(f"No state file for {user!r} — creating fresh state from profile")
        st = default_state(profile)
        save_state(user, st)
        return st

    try:
        with path.open("r", encoding="utf-8") as f:
            st = json.load(f)
        for key in ("current_books", "marks", "history"):
            if key not in st:
                raise ValueError(f"missing key: {key}")
        return st
    except Exception as e:
        backup = path.with_suffix(f".corrupt-{int(time.time())}.json")
        log.error(f"State file for {user!r} corrupt ({e}). Backing up to {backup.name} and starting fresh.")
        try:
            path.rename(backup)
        except Exception as ee:
            log.error(f"Could not rename corrupt state file: {ee}")
        st = default_state(profile)
        save_state(user, st)
        return st


def save_state(user: str, state: dict[str, Any]) -> None:
    user_dir(user).mkdir(parents=True, exist_ok=True)
    path = state_path(user)
    tmp = path.with_suffix(".tmp")
    with tmp.open("w", encoding="utf-8") as f:
        json.dump(state, f, indent=2, ensure_ascii=False)
    tmp.replace(path)
    log.debug(
        f"saved state for {user!r}: {len(state['current_books'])} books, "
        f"{len(state['marks'])} marks, {len(state['history'])} history"
    )


# ── First-run migration ──

def migrate_legacy_if_needed() -> None:
    """Ensure the old single-user setup survives as user 'nick'.

    Two cases, both idempotent (safe to call on every startup):

    1. Upgrade from the old single-user app: a state.json exists but nick has
       not been created yet. Nick's hand-crafted profile becomes
       users/nick/profile.json and the existing state.json (his marks, history
       and current list) becomes users/nick/state.json — nothing is lost. The
       old state.json is then renamed to a timestamped backup.

    2. A completely fresh install (no state.json and no users at all): seed a
       default 'nick' from the built-in book list, matching the app's previous
       first-run behaviour so there is always something to open.

    Once nick exists and the legacy file has been backed up, neither case
    fires again.
    """
    legacy_pending = LEGACY_STATE_FILE.exists() and not user_exists(DEFAULT_USER)
    fresh_install = not USERS_DIR.exists() and not LEGACY_STATE_FILE.exists()
    if not (legacy_pending or fresh_install):
        return

    log.info("Setting up multi-user storage — ensuring user 'nick' exists")
    profile = default_profile("Nick")
    save_profile(DEFAULT_USER, profile)

    if legacy_pending:
        try:
            with LEGACY_STATE_FILE.open("r", encoding="utf-8") as f:
                legacy = json.load(f)
            for key in ("current_books", "marks", "history"):
                if key not in legacy:
                    raise ValueError(f"legacy state missing key: {key}")
            save_state(DEFAULT_USER, legacy)
            backup = LEGACY_STATE_FILE.with_name(f"state.json.pre-multiuser-{int(time.time())}")
            LEGACY_STATE_FILE.rename(backup)
            log.info(
                f"  migrated existing state ({len(legacy['current_books'])} books, "
                f"{len(legacy['history'])} history) → {state_path(DEFAULT_USER)}"
            )
            log.info(f"  old state.json preserved as {backup.name}")
        except Exception as e:
            log.error(f"Could not migrate legacy state.json ({e}); starting nick from seed books")
            save_state(DEFAULT_USER, default_state(profile))
    else:
        log.info("  fresh install — seeding nick from the built-in book list")
        save_state(DEFAULT_USER, default_state(profile))


