def resolve_user(requested: str | None) -> str:
    """Decide which user to run as.

    Precedence: explicit --user, then the last-used user, then 'nick', then the
    sole user if there is exactly one. Exits with a helpful message otherwise.
    """
    available = list_users()

    if requested is not None:
        try:
            user = normalise_user_name(requested)
        except ValueError as e:
            log.error(str(e))
            sys.exit(1)
        if not user_exists(user):
            log.error(f"No such user: {user!r}.")
            if available:
                log.error(f"Available users: {', '.join(available)}")
            log.error(f"Create one with:  python server.py --new-user {user}")
            sys.exit(1)
        return user

    last = read_last_user()
    if last and user_exists(last):
        return last

    if user_exists(DEFAULT_USER):
        return DEFAULT_USER

    if len(available) == 1:
        return available[0]

    log.error("No user could be selected.")
    if available:
        log.error(f"Pick one with --user. Available users: {', '.join(available)}")
    else:
        log.error("There are no users yet. Create one with:  python server.py --new-user NAME")
    sys.exit(1)


